#include "push_swap.h"


void	arg_check(char **str)
{
	int i;
	int x;

	i = 1;
	while (str[i])
	{
		x = 0;
		while (str[i][x])
		{
			if (!(str[i][x] >= '0' && str[i][x] <= '9'))
			{
				printf("Error\n");
				exit(-1);
			}
			x++;
		}
		i++;
	}
}
